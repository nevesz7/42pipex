#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char			*join;
	unsigned int	i;
	unsigned int	len;

	len = ft_strlen(s1);
	join = malloc(len + ft_strlen(s2) + 1);
	if (!join)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		join[i] = s1[i];
		i++;
	}
	i = 0;
	while (s2[i])
	{
		join[len + i] = s2[i];
		i++;
	}
	join[len + i] = '\0';
	return (join);
}
