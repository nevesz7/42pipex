#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char	*char_dest;
	char	*char_src;

	char_dest = (char *)dest;
	char_src = (char *)src;
	while (n-- > 0)
	{
		*char_dest = *char_src;
		char_dest++;
		char_src++;
	}
	return (dest);
}
