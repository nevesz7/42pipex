#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = -1;
	while (s[++i] || s[i] == c)
		if (s[i] == (char)c)
			return ((char *)&s[i]);
	return (NULL);
}
